                        ក្រោយពេលដាក់ចំណីនៅពេលព្រឹក និងពេលល្ងាច ។ ការចាប់ផ្តើមរកឈ្មោលរបស់ជ្រូកមេ វាអាស្រ័យទៅតាមពូជ ។ មេជ្រូកក្នុងស្រុក
                        (ពូជ ក្នុងស្រុក) រកឈ្មោលនៅអាយុ ៣-៤ ខែ ទំងន់ ៤៥-៥០ គ.ក ។ រឺឯពូជនាំចូលវិញ រកឈ្មោល នៅអាយុ ៦-៧ ខែ ទំងន់
                        ៦០-៧០ គ.ក ។ ការបង្កាត់ពូជជ្រូក មាន ២ របៀប តាមធម្មជាតិ និងតាមសិប្បនិម្មិត
                        <br> ១. ការបង្កាត់តាមធម្មជាតិ : នៅពេលជ្រូកមេក្រមុំអាយុបាន៥-៦ ខែ ពួកវាចាប់ផ្តើម រកឈ្មោលលើកទី ១។ អាយុក្មេងជាងនេះ
                        ជ្រូកមេក្រមុំ គឺមិនសមស្របទេសំរាប់បង្កាត់ ពូជ ពីព្រោះសរីរាង្គបន្តពូជរបស់ពួកវាមិនទាន់លូតលាស់បានល្អ
                        ។ ជ្រូកមេត្រូវតែបង្កាត់ ពូជនៅពេលដែលវាមានអាយុ ៧-៨ ខែ និងចាប់រកឈ្មោលលើកទី ២ និងលើកទី ៣។ រយៈពេលជាមធ្យមនៃការរកឈ្មោល
                        គឺ ២១ ថ្ងៃ ហើយវដ្តនេះកើតឡើងម្តងទៀត នៅពេលជ្រូកមេដាក់បាមិនជាប់ ។
                        <br> ២. ការបង្កាត់បែបសិប្បនិម្មិតលើជ្រូក : នៅពេលប្រើ ការបង្កាត់តាមបែបសិប្បនិម្មិតជ្រូកបាមិនបង្កាត់ ដោយផ្ទាល់ជាមួយជ្រូកមេទេ
                        ។ ជាមួយនឹងការ បង្កាត់តាមបែបសិប្បនិម្មិត . ឆ្កែម ដែលបានពីជ្រូក ជា ត្រូវបានគេប្រមូលទុក រួចហើយដាក់បញ្ចូលតាម
                        រយៈប្រដាប់បញ្ចូលល្ហែមទៅក្នុងសួនរបស់មេជ្រូក។
